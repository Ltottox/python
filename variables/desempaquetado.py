#el desepmpaquetado de variables es asignar los valores de una lista o tupla a varias variables en una sola línea de código.
#ejemplo con lista
lista = ["Antonio", 30, "estudiante", 1.72]
nombre, edad, ocupacion, altura = lista #desempaquetado de lista
print(f"Nombre: {nombre}, Edad: {edad}, Ocupacion: {ocupacion}, Altura: {altura}")